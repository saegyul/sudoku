def gen_puzzle():
    b1 = Board()
    solver.generate_puzzle(b1.bd)
    b1.display(stop=True)

    ans = []
    solver.find_solution(b1,ans)
    for bd in ans:
        Board(bd).show()
        c = input("next : ")

def sol_puzzle():
    b2 = Board()
    b2.fillFromUser()
    b2.display(stop=True)

    ans = []
    solver.find_solution(b2, ans)

    for bd in ans:
        Board(bd).show()
        c = input("more answer ?")

